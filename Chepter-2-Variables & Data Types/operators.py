# 1. Arethmetic Operators. (+ , - , * , / , %)
a=100 # First Value
b=50 # Second Value
print("Addition Of Value :",(a+b))
print("Subtraction Of Value :",(a-b))
print("Multiplication Of Value :",(a*b))
print("Division Of Value :",(a/b))
print("Reminder Of Value :",(a%b))
# 2. Assignment Operators. (=, += , -= , *= , /= )
ass_val=10
print(" Assign Value in Variable :",ass_val)
ass_val+=10 
print(" Assign Value in Variable :",ass_val)
ass_val-=10
print(" Assign Value in Variable :",ass_val)
ass_val*=10
print(" Assign Value in Variable :",ass_val)
ass_val/=10
print(" Assign Value in Variable :",ass_val)
#3. Comparison Operators. (> , < , >= , <= , == , !=)(Output Is either True OR False)
value_1=3
value_2=5
print("value_1 is bigger than value_2:",(value_1>value_2))
print("value_1 is Smaller than value_2:",(value_1<value_2))
print("value_1 is big or equal than value_2:",(value_1>=value_2))
print("value_1 is Small or equal than value_2:",(value_1<=value_2))
print("value_1 is equal to value_2:",(value_1==value_2))
print("value_1 is not equal to value_2:",(value_1!=value_2))
#4. Logical Operators. (And , or , not)
log_1=True
log_2=False
print("log_1 and log_2 :",(log_1 and log_2))
print("log_1 or log_2 :",(log_1 or log_2))
print("Nagative Value Of log_1:",(not log_1))
